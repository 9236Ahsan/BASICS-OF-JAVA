// Write the program to print sum, multiply, divide and subtraction of two
// numbers using constant values

public class Assis3 {
    public static void main(String args[]){
        int x=4;
        int y=2;

        
        System.out.println(" sum of "+x+" And "+y+" is "+(x+y));
        System.out.println(" Multiply of "+x+" And "+y+" is "+(x*y));
        System.out.println(" Divide of "+x+" And "+y+" is "+(x/y));
        System.out.println(" Substraction of "+x+" And "+y+" is "+(x-y));

    }
    
}
